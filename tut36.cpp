/*
WHY OOPS(Object Oriented Programming Style)?
* C++ launguage was designed with the main intention of adding object-oriented features to C launguage.
*As the size od the program increases, readibility, maintainability and bug-free nature of programs decreases.
*This was the major problem with launguage like C which relied upon functions or procedures 
(hence the name procedual programming launguage).
*As a result, the possibility of not addressing the problem in effective manner was high.
*Also, as data was almost neglected, data security was easily compromised.
*Using classes solves this problems by modelling program as a real world scenario.
*/

/*
PROCEDURE ORIENTED PROGRAMMING(POP) (Jo Ab Tak Dekhi Humne)
*Consists of writing a set of instructions for the computer to follow
*Main focus is on functions and not on flow of data
*Functions can either use local or global data
*Data moves openly from function to function
*/

/*
OBJECT ORIENTED PROGRAMMING(OOPS)
*Works on the concept of classes and objects
*A class is a template to create objects
*Treat data as a critical element\
*Decomposes the problem in objects and build data and functions around the objects
*/

/*
BASIC CONCEPTS IN OBJECT ORIENTED PROGRAMMING
*Classes - Basic template for creating objects
*Objects - Basic run time entities
*Data Abstraction & Encapsulation - Wrapping data and functions into single unit
*Inheritance - Propertiens of one class can be inherited into otthers
*Polymorphism - ability to take more than one forms
*Dynamic Binding - code which will execute is not known until the program runs
*Message Passing - Objects message (Information) call format
*/

/*
BENEFITS OF OBJECT ORIENTED PROGRAMMING
*Better code reuseability using objects and Inheritance
*Principle of data hiding build secude systems
*Multiple objects can co-exist without any interference
*Software complexity can be easily managed
*/